#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		len;
	char	*res;

	if (s)
	{
		i = 0;
		len = ft_strlen(s);
		if (!(res = (char*)malloc(sizeof(*res) * (len + 1))))
			return (NULL);
		while (i < len)
		{
			res[i] = f(i, s[i]);
			i++;
		}
		res[i] = '\0';
		return (res);
	}
	return (NULL);
}
