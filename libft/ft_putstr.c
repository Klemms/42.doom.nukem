#include "libft.h"
#include <unistd.h>

void	ft_putstr(char const *s)
{
	size_t i;

	if (s == NULL)
		return ;
	i = 0;
	while (s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
}
